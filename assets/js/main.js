function initTagClickEvents() {
    console.log('üè∑Ô∏è ÌÉúÍ∑∏ ÌÅ¥Î¶≠ Ïù¥Î≤§Ìä∏ Ï¥àÍ∏∞Ìôî');
    
    // Ïù¥Î≤§Ìä∏ Îç∏Î¶¨Í≤åÏù¥ÏÖò ÏÇ¨Ïö©
    document.body.addEventListener('click', function(e) {
        // ÌÅ¥Î¶≠Îêú ÏöîÏÜåÍ∞Ä ÌÉúÍ∑∏Ïù∏ÏßÄ ÌôïÏù∏
        if (e.target.classList.contains('tag-clickable') || 
            e.target.classList.contains('post-tag') ||
            e.target.classList.contains('post-list-tag')) {
            
            e.preventDefault();
            
            const tagName = e.target.getAttribute('data-tag') || e.target.textContent.trim();
            console.log('ÌÉúÍ∑∏ ÌÅ¥Î¶≠Îê®:', tagName);
            
            if (tagName) {
                const searchUrl = `/search/?q=${encodeURIComponent(tagName)}`;
                console.log('Ïù¥ÎèôÌï† URL:', searchUrl);
                window.location.href = searchUrl;
            }
        }
    });
    
    // ÌÉúÍ∑∏ ÏöîÏÜåÎì§Ïóê Ïª§ÏÑú Ïä§ÌÉÄÏùº Ï†ÅÏö©
    function applyTagStyles() {
        const tagElements = document.querySelectorAll('.tag-clickable, .post-tag, .post-list-tag');
        console.log('ÌÉúÍ∑∏ ÏöîÏÜå Í∞úÏàò:', tagElements.length);
        
        tagElements.forEach(tag => {
            tag.style.cursor = 'pointer';
            tag.style.transition = 'all 0.2s ease';
            
            // Ìò∏Î≤Ñ Ìö®Í≥º Ï∂îÍ∞Ä
            tag.addEventListener('mouseenter', function() {
                this.style.transform = 'translateY(-1px)';
                this.style.boxShadow = '0 4px 8px rgba(0,0,0,0.1)';
            });
            
            tag.addEventListener('mouseleave', function() {
                this.style.transform = 'translateY(0)';
                this.style.boxShadow = 'none';
            });
        });
    }
    
    // Ï¥àÍ∏∞ Ï†ÅÏö©
    applyTagStyles();
    
    // ÏÉàÎ°ú Ï∂îÍ∞ÄÎêú ÏöîÏÜåÎì§ÏóêÎèÑ Ï†ÅÏö©
    const observer = new MutationObserver(function(mutations) {
        mutations.forEach(function(mutation) {
            if (mutation.type === 'childList' && mutation.addedNodes.length > 0) {
                applyTagStyles();
            }
        });
    });
    
    observer.observe(document.body, {
        childList: true,
        subtree: true
    });
    
    console.log('‚úÖ ÌÉúÍ∑∏ ÌÅ¥Î¶≠ Ïù¥Î≤§Ìä∏ Ï¥àÍ∏∞Ìôî ÏôÑÎ£å');
}

